interface I18nContextType {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (key: string, replacements?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nContextType | null>(null);

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error('useI18n must be within I18nProvider');
  return ctx;
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    const saved = loadLocale() as Locale | null;
    return saved && translations[saved] ? saved : 'ru';
  });

  const setLocale = useCallback((l: Locale) => {
    setLocaleState(l);
    saveLocale(l as StoredLocale);
  }, []);

  const t = useCallback((key: string, replacements?: Record<string, string | number>): string => {
    let s = translations[locale]?.[key] || translations.ru[key] || key;
    if (replacements) {
      for (const [k, v] of Object.entries(replacements)) {
        s = s.split(`{{${k}}}`).join(String(v));
      }
    }
    return s;
  }, [locale]);

  return (
    <I18nContext.Provider value={{ locale, setLocale, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function getLocale(): Locale {
  const saved = loadLocale() as Locale | null;
  return saved && translations[saved] ? saved : 'ru';
}

export function t(key: string, replacements?: Record<string, string | number>): string {
  const locale = getLocale();
  let s = translations[locale]?.[key] || translations.ru[key] || key;
  if (replacements) {
    for (const [k, v] of Object.entries(replacements)) {
      s = s.split(`{{${k}}}`).join(String(v));
    }
  }
  return s;
}
